    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            HttpSession session = request.getSession();
            UserDTO loginUser = (UserDTO) session.getAttribute("LOGIN_USER");

            if (loginUser != null) {
                int userId = loginUser.getId(); // Assuming getUserId() returns the userId
                OrderDAO orderDAO = new OrderDAO();
                List<OrderDTO> orders = orderDAO.getOrdersByUserId(userId);

                // Set orders attribute to be accessed in the JSP
                request.setAttribute("orders", orders);
                request.getRequestDispatcher("myorder.jsp").forward(request, response);
            } else {
                // Handle case where user is not logged in
                response.sendRedirect("login.jsp");
            }
        } catch (SQLException | ClassNotFoundException ex) {
            // Handle any exceptions
            ex.printStackTrace();
            request.setAttribute("error", "An error occurred while retrieving orders.");
            request.getRequestDispatcher("error.jsp").forward(request, response);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

